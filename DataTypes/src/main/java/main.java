public class main {
    public static void main(String[] args) {
        System.out.println("\nExample 1: Java boolean data type");
        boolean dot = true;
        System.out.println(dot);    // prints true

        System.out.println("\nExample 2: Java byte data type");
        byte range;
        range = 124;
        System.out.println(range);    // prints 124

        System.out.println("\nExample 3: Java short data type");
        short temperature;
        temperature = -200;
        System.out.println(temperature);  // prints -200

        System.out.println("\nExample 4: Java int data type");
        int row = -4250000;
        System.out.println(row);  // print -4250000

        System.out.println("\nExample 5: Java long data type");
        long line = -42332200000L;
        System.out.println(line);    // prints -42332200000

        System.out.println("\nExample 6: Java double data type");
        double number = -42.3;
        System.out.println(number);  // prints -42.3

        System.out.println("\nExample 7: Java float data type");
        float stitch = -42.3f;
        System.out.println(stitch);  // prints -42.3

        System.out.println("\nExample 8: Java char data type");
        char letter = '\u0051';
        System.out.println(letter);  // prints Q

        //- Here is another example:
        System.out.println("\nHere is another example:");
        char letter1 = '9';
        System.out.println(letter1);  // prints 9
        char letter2 = 65;
        System.out.println(letter2);  // prints A
    }
}
